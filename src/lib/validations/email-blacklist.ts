/**
 * Check if an email domain is in the temporary email blacklist
 * @param email - Email address to check
 * @returns boolean - true if domain is blacklisted (temporary email)
 */
export function isTemporaryEmailDomain(email: string): boolean {
  if (!email || !email.includes('@')) {
    return false;
  }
  
  const domain = email.split('@')[1]?.toLowerCase();
  if (!domain) {
    return false;
  }
  
  return TEMPORARY_EMAIL_DOMAINS.includes(domain);
}

/**
 * Get a user-friendly error message for blacklisted email domains
 * @param language - 'id' for Indonesian, 'en' for English
 * @returns string - Error message
 */
export function getTemporaryEmailErrorMessage(language: 'id' | 'en' = 'id'): string {
  if (language === 'en') {
    return 'Temporary email addresses are not allowed. Please use a permanent email address for better communication and order updates.';
  }
  
  return 'Email sementara tidak diperbolehkan. Gunakan email permanen agar kami dapat mengirim update pesanan dan berkomunikasi dengan baik.';
}

/**
 * Validate email and check against blacklist
 * @param email - Email address to validate
 * @param language - Language for error messages
 * @returns object with isValid boolean and error message if invalid
 */
export function validateEmailDomain(email: string, language: 'id' | 'en' = 'id') {
  // Basic email format validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  
  if (!email) {
    return {
      isValid: false,
      error: language === 'en' ? 'Email is required' : 'Email wajib diisi'
    };
  }
  
  if (!emailRegex.test(email)) {
    return {
      isValid: false,
      error: language === 'en' ? 'Please enter a valid email address' : 'Masukkan alamat email yang valid'
    };
  }
  
  if (isTemporaryEmailDomain(email)) {
    return {
      isValid: false,
      error: getTemporaryEmailErrorMessage(language)
    };
  }
  
  return {
    isValid: true,
    error: null
  };
}